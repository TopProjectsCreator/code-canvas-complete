export const getPlaybooksForRule = (ruleId: string): SafetyPlaybook[] => {
  const playbookIds = PLAYBOOKS_BY_RULE_ID[ruleId] ?? [];
  return playbookIds
    .map((playbookId) => SAFETY_PLAYBOOKS.find((playbook) => playbook.id === playbookId))
    .filter((playbook): playbook is SafetyPlaybook => Boolean(playbook));
};

export const summarizePlaybook = (playbook: SafetyPlaybook): string => {
  const keyActions = playbook.checklist.slice(0, 3).join(' · ');
  return `${playbook.title}: ${keyActions}`;
};

export const listPlaybookTitles = (): string[] => {
  return SAFETY_PLAYBOOKS.map((playbook) => playbook.title);
};
